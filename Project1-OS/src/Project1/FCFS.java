    private void calcBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcBtnActionPerformed

        DefaultTableModel d=(DefaultTableModel) processesTable.getModel();
        rowCount =d.getRowCount();
        int bursttime =(int) d.getValueAt(0, 2);
        int activetime =(int) d.getValueAt(0, 1);
        int completionTime = 0;
        int turnaround = 0;
        int totalTurnaround = 0;
        completionTime = bursttime + activetime;
        for(int i=0;i<rowCount;i++){
            int proId = (int) d.getValueAt(i, 0);
            activetime =(int) d.getValueAt(i, 1);
            bursttime =(int) d.getValueAt(i, 2);

            turnaround=completionTime - activetime;
            completionTime=completionTime + bursttime;
            table2_update(proId, turnaround);
            totalTurnaround = totalTurnaround + turnaround;
    }
        String total =String.valueOf(totalTurnaround);
        totTurnaround.setText(total);
        float res = 0;
        res = totalTurnaround/rowCount;
        String avg =String.valueOf(res);
        avgTurnaround.setText(avg);
    }//GEN-LAST:event_calcBtnActionPerformed

