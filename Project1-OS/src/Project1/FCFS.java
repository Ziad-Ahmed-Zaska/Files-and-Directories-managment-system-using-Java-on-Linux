    private void table_update(int process_ID, int arrive_time, int burst_time){
    
        DefaultTableModel d=(DefaultTableModel) processesTable.getModel();
        Vector v2 = new Vector();
        v2.add(process_ID);
        v2.add(arrive_time);
        v2.add(burst_time);
        d.addRow(v2);
        rowCount =d.getRowCount();
}
